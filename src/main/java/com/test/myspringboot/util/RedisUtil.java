    /**
     * 通过SETNX试图获取一个锁
     *
     * @param key
     * @param expire 存活时间(秒)
     * @return
     */
    public Boolean acquireLock(String key, final long expire) {
        final byte[] _key = key.getBytes();
        boolean flag = redisTemplate.execute(new RedisCallback<Boolean>() {
            @Override
            public Boolean doInRedis(RedisConnection connection) throws DataAccessException {
                try {
                    Long value = System.currentTimeMillis() + (expire * 1000) + 1;
                    byte[] _value = ToolsUtils.object2Byte(value);
                    boolean isDo = connection.setNX(_key, _value);
                    if (isDo) { // SETNX成功，则成功获取一个锁
                        if (expire > 0) {
                            connection.expire(_key, expire);
                        }
                        return Boolean.TRUE;
                    } else { // SETNX失败，说明锁仍然被其他对象保持，检查其是否已经超时
                        Long oldValue = (Long) ToolsUtils.byte2Object(connection.get(_key));
                        if (oldValue.longValue() < System.currentTimeMillis()) { // 超时
                            Long getValue = (Long) ToolsUtils.byte2Object(connection.getSet(_key, _value));
                            if (getValue.longValue() == oldValue.longValue()) { // 获取锁成功
                                return Boolean.TRUE;
                            } else { // 已被其他进程捷足先登了
                                return Boolean.FALSE;
                            }
                        } else { // 未超时，则直接返回失败
                            return Boolean.FALSE;
                        }
                    }
                } catch (Exception e) {
                    return Boolean.FALSE;
                }
            }

        });
        RedisConnectionUtils.unbindConnection(redisTemplate.getConnectionFactory());
        return flag;
    }

    /**
     * 释放锁
     *
     * @param key
     */
    public void releaseLock(String key) {
        final byte[] _key = key.getBytes();
        redisTemplate.execute(new RedisCallback<Object>() {
            @Override
            public Object doInRedis(RedisConnection connection) throws DataAccessException {
                byte[] value = connection.get(_key);
                if (value == null) {
                    return null;
                }
                Long getValue = (Long) ToolsUtils.byte2Object(value);
                // 避免删除非自己获取得到的锁
                if (System.currentTimeMillis() < getValue.longValue()) {
                    connection.del(_key);
                }
                return null;
            }

        });
        RedisConnectionUtils.unbindConnection(redisTemplate.getConnectionFactory());
    }

